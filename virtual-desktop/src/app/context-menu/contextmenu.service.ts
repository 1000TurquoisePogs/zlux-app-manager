import { Injectable, EventEmitter } from '@angular/core';

@Injectable()
export class ContextMenuService {
  public closeEvent = new EventEmitter<void>();
  public createEvent = new EventEmitter<void>();

  public onClose () {
    this.closeEvent.next();
  }

  public onCreate () {
    this.createEvent.next();
  }
}

